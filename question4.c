//program for expressions 10&&8,0&&8,0&&0,8&&0  
#include <stdio.h>
int main()
{
    printf("The return value of the 10&&8 is %d\n",10&&8);
    printf("The return value of the 0&&8 is %d\n",0&&8);
    printf("The return value of the 0&&0 %d\n",0&&0);
    printf("the return value of the 8&&0 %d\n",8&&0);
    return 0;
}